def isValidSudoku(board: list) -> bool:
    d = dict()
    for i in range(len(board)):
        for j in range(len(board[i])):
            if board[i][j] in d:
                d[board[i][j]].append([i, j])
            elif board[i][j] != '.':
                d[board[i][j]] = [[i, j]]

    for k in d:
        tmp = dict()
        for m in d[k]:
            if str('x') + str(m[0]) in tmp:
                return False
            elif str('y') + str(m[1]) in tmp:
                return False
            elif str(m[0] // 3) + str(m[1] // 3) in tmp:
                return False
            else:
                tmp[str('x') + str(m[0])] = 1
                tmp[str('y') + str(m[1])] = 1
                tmp[str(m[0] // 3) + str(m[1] // 3)] = 1
    return True


